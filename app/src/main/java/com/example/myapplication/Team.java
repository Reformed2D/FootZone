package com.example.myapplication;

public class Team {
    private int teamId;
    private String teamName;
    private String goalkeeper;
    private String defenderLeft;
    private String defenderRight;
    private String midfielderLeft;
    private String midfielderRight;
    private String forwardLeft;
    private String forwardRight;

    // Getters and setters
    public int getTeamId() { return teamId; }
    public void setTeamId(int teamId) { this.teamId = teamId; }

    public String getTeamName() { return teamName; }
    public void setTeamName(String teamName) { this.teamName = teamName; }

    public String getGoalkeeper() { return goalkeeper; }
    public void setGoalkeeper(String goalkeeper) { this.goalkeeper = goalkeeper; }

    public String getDefenderLeft() { return defenderLeft; }
    public void setDefenderLeft(String defenderLeft) { this.defenderLeft = defenderLeft; }

    public String getDefenderRight() { return defenderRight; }
    public void setDefenderRight(String defenderRight) { this.defenderRight = defenderRight; }

    public String getMidfielderLeft() { return midfielderLeft; }
    public void setMidfielderLeft(String midfielderLeft) { this.midfielderLeft = midfielderLeft; }

    public String getMidfielderRight() { return midfielderRight; }
    public void setMidfielderRight(String midfielderRight) { this.midfielderRight = midfielderRight; }

    public String getForwardLeft() { return forwardLeft; }
    public void setForwardLeft(String forwardLeft) { this.forwardLeft = forwardLeft; }

    public String getForwardRight() { return forwardRight; }
    public void setForwardRight(String forwardRight) { this.forwardRight = forwardRight; }
}
